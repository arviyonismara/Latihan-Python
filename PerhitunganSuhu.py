# Latihan perhitungan suhu

#celcius
celcius = float(input("masukan suhu dalam celcius : "))
print("suhu celcius adalah : ",celcius,"C\n")

#reamur
reamur = (4/5) * celcius
print("suhu reamur adalah : ",reamur,"R\n")

#farenheit
farenheit = (9/5) * celcius + 32
print("suhu farenheit adalah : ",farenheit,"R\n")

#Kelvin
kelvin = celcius + 273
print("suhu kelvin adalah : ",kelvin,"R\n")