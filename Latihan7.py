#latihan Input

#input data String
data = input("masukan data: ") #jika seperti ini data inputan pasti betipe string
print("data = ",data,type(data))

#untuk inputan bertipe int
angka = int(input("masukan angka: "))
print("data = ",angka,type(angka))

#untuk inputan bertipe int
float = float(input("masukan float: "))
print("data = ",float,type(float))

#untuk inputan bertipe bool
biner = bool(int(input("masukan angka: ")))
print("data = ",biner,type(biner))